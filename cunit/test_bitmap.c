void test_bitmap(void)
{
	uint8 ldecompressed_16x1x8[16 * 1 * 1];
	uint8 ldecompressed_32x32x8[32 * 32 * 1];
	int width;
	int height;
	int comp_size;
	int decomp_size;
	int bpp;

	width = 16;
	height = 1;
	bpp = 8;
	comp_size = sizeof(compressed_16x1x8);
	decomp_size = sizeof(decompressed_16x1x8);
	CU_ASSERT(bitmap_decompress(0, ldecompressed_16x1x8, width, height,
		compressed_16x1x8, comp_size, bpp, bpp) == 0);
	CU_ASSERT(memcmp(ldecompressed_16x1x8, decompressed_16x1x8,
		decomp_size) == 0);

	width = 32;
	height = 32;
	bpp = 8;
	comp_size = sizeof(compressed_32x32x8);
	decomp_size = sizeof(decompressed_32x32x8);
	CU_ASSERT(bitmap_decompress(0, ldecompressed_32x32x8, width, height,
		compressed_32x32x8, comp_size, bpp, bpp) == 0);
	CU_ASSERT(memcmp(ldecompressed_32x32x8, decompressed_32x32x8,
		decomp_size) == 0);
}
